# Create a Set:

thisset = {"apple", "banana", "cherry"}
print(thisset)


# Duplicate values will be ignored:

thisset = {"apple", "banana", "cherry", "apple"}
print(thisset)


# True and 1 is considered the same value:

thisset = {"apple", "banana", "cherry", True, 1, 2, False,0}
print(thisset)
